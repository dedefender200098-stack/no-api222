from rich.console import Console
from rich.text import Text


def print_logo(console: Console):
    logo = r'''
┌────────────────────────────────────────────────────────────────────────────────────┐
│    ______              ______   __                                                 │
│   /      \            /      \ |  \                                                │
│  |  $$$$$$\ __    __ |  $$$$$$\| $$____    ______    ______    ______   __    __   │
│  | $$    $$|  \  /  \| $$   \$$| $$    \  /      \  /      \  /      \ |  \  |  \  │
│  | $$    $$ \$$\/  $$| $$      | $$$$$$$\|  $$$$$$\|  $$$$$$\|  $$$$$$\| $$  | $$  │
│  | $$    $$  >$$  $$ | $$   __ | $$  | $$| $$    $$| $$   \$$| $$   \$$| $$  | $$  │
│  | $$    $$ /  $$$$\ | $$__/  \| $$  | $$| $$$$$$$$| $$      | $$      | $$__/ $$  │
│   \$$    $$|  $$ \$$\ \$$    $$| $$  | $$ \$$     \| $$      | $$       \$$    $$  │
│    \$$$$$$  \$$   \$$  \$$$$$$  \$$   \$$  \$$$$$$$ \$$       \$$       _\$$$$$$$  │
│                                                                        |  \__| $$  │
│                                                                         \$$    $$  │
│                                                                          \$$$$$$   │
│                                                                                    │
├────────────────────────────────────────────────────────────────────────────────────┤
│                         Версия софта от 18:00 03.11.2025                           │
└────────────────────────────────────────────────────────────────────────────────────┘
    '''
    for i, line in enumerate(logo.splitlines()):
        text = Text(line)

        for idx, ch in enumerate(line):
            if ch not in ("│"):
                text.stylize("bold grey100", idx, idx + 1)
            if ch in ("│", "┘", "└", "─", "├", "┤", "┌", "┐"):
                text.stylize("bold light_coral", idx, idx + 1)
            if ch in ("$", "\\", ">", "|", "/", "_"):
                if idx > 22:
                    text.stylize("bold red1", idx, idx + 1)
                else:
                    text.stylize("bold bright_green", idx, idx + 1)

        console.print(text)
